namespace app\admin\model\user;

use think\Model;

class UserGroup extends Model
{
    public static function onbeforeDelete($row)
    {
        if ($row['issystem']) {
            throw new \Exception('系统用户组[' . $row['name'] . ']不能删除！');
        }
    }
}
